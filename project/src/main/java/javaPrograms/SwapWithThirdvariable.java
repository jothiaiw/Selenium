package javaPrograms;

public class SwapWithThirdvariable {
	public static void main(String[] args) {
	
		int a = 10, b= 20 , c;
		
		System.out.println("Before swap : a = " + a + ", b = " + b);	
		
		c =a ;
		
		a= b;
		 
		b= c;
		
		System.out.println("After swap : a = " + a + ", b = " + b);
		
		
		
	}
}

